package com.izanagicraft.guard.events.listener;

import com.izanagicraft.guard.IzanagiWorldGuardPlugin;
import com.izanagicraft.guard.commands.BuildModeCommand;
import com.izanagicraft.guard.events.GuardListener;
import com.izanagicraft.guard.utils.MessageUtils;
import org.bukkit.Bukkit;
import org.bukkit.event.EventHandler;
import org.bukkit.event.player.PlayerJoinEvent;

/**
 * IzanagiWorldGuard; com.izanagicraft.guard.events.listener:PlayerJoinListener
 *
 * @author <a href="https://github.com/sanguine6660">@sanguine6660</a>
 * @since 19.12.2023
 */
public class PlayerJoinListener extends GuardListener {
    /**
     * Constructs a new GuardListener with the specified IzanagiWorldGuardPlugin.
     *
     * @param plugin The IzanagiWorldGuardPlugin instance.
     */
    public PlayerJoinListener(IzanagiWorldGuardPlugin plugin) {
        super(plugin);
    }

    /**
     * Handles the PlayerJoinEvent to enforce flags and utility things
     *
     * @param event The PlayerJoinEvent
     */
    @EventHandler
    public void joined(PlayerJoinEvent event) {
        Bukkit.getScheduler().runTaskLater(getPlugin(), () -> {
            if (BuildModeCommand.buildModePlayers.contains(event.getPlayer().getUniqueId())) {
                MessageUtils.sendPrefixedMessage(event.getPlayer(), "&eBe careful with your actions you &ajoined &ethe build mode.");
            }
        }, 20);
    }
}
